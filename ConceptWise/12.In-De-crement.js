let num1 = 0;
// Post increment
console.log("Post increment--> ", num1++)
console.log(num1)
// Pre increment
let num2 = 0;
console.log("Pre increment--> ", ++num2)
console.log(num2)


let num3 = 0;
// Post decrement
console.log("Post decrement--> ", num3--)
console.log(num3)
// Pre decrement
let num4 = 0;
console.log("Pre decrement--> ", --num4)
console.log(num4)